/* ==================================================
 * Graph for SAO using SDDL1 (original SDDL)
 * ==================================================
 */

ZL_GraphID sao_graph_sddl1(ZL_Compressor* cgraph)
{
    // Set format version and compression level
    if (ZL_isError(ZL_Compressor_setParameter(
                cgraph, ZL_CParam_formatVersion, ZL_MAX_FORMAT_VERSION))) {
        abort();
    }
    if (ZL_isError(ZL_Compressor_setParameter(
                cgraph, ZL_CParam_compressionLevel, 1))) {
        abort();
    }

    // Register SDDL1 graph with STORE destination for benchmarking
    ZL_Result_ZL_GraphID result = ZL_Compressor_buildSDDLGraph(
            cgraph,
            sao_sddl1_bytecode,
            sao_sddl1_bytecode_size,
            ZL_GRAPH_STORE);

    // Check if the result contains an error
    if (result._value._code != ZL_ErrorCode_no_error) {
        abort();
    }

    ZL_GraphID sddl1_graph = result._value._value;
    if (!ZL_GraphID_isValid(sddl1_graph)) {
        abort();
    }

    return sddl1_graph;
}
